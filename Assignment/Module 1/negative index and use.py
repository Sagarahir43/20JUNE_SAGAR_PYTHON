"""In Python, negative indexes are used to access elements in a sequence (such as a string, list, or tuple)
from the end of the sequence rather than from the beginning. Negative indexes count positions backward, 
with -1 referring to the last element, -2 referring to the second-to-last element, and so on.
"""
#Here's an example to illustrate the use of negative indexes

str = ("hello python")
print(str[-3:])